## numeric data type :

# int 
marks = 89
print(marks)

# float 
height = 5.3
print(height)

# complex 
z = 5 + 3j
print(z)


## sequence data type 

# string 

name = "Anjali patel"
print(name)

subject = 'python programing'
print(subject)

# list 

l1 = [1,2,3]
print(l1)

# tuple

t1 = (1,2,3)
print(t1)

# range 

r = range(6)
print(r)


## map data type 

# map - dictionary 

d = {"subject" : "python"}
print(d)

# set data type

s = {1,2,3}
print(s)

# boolean data type 

status = True 
print(status)

