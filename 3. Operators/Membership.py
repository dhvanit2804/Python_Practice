'''
Used to test if a value is part of a sequence (like list, string, tuple).

Operator	Example	             Output
in	        'a' in 'apple'	     True
not in	    'b' not in 'apple'	 True
'''

fruits = ["apple", "banana", "mango"]
print("apple" in fruits)      # True
print("grape" not in fruits)  # True
